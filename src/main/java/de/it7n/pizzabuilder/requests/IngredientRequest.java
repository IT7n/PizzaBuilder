package de.it7n.pizzabuilder.requests;

/**
 * @author Collin Alpert
 */
public class IngredientRequest {

	private int mozzarella;
	private int olive;
	private int mushroom;
	private int pepperoni;
	private int redPepper;
	private int greenPepper;
	private int tomato;

	public int getMozzarella() {
		return mozzarella;
	}

	public int getOlive() {
		return olive;
	}

	public int getMushroom() {
		return mushroom;
	}

	public int getPepperoni() {
		return pepperoni;
	}

	public int getRedPepper() {
		return redPepper;
	}

	public int getGreenPepper() {
		return greenPepper;
	}

	public int getTomato() {
		return tomato;
	}
}
